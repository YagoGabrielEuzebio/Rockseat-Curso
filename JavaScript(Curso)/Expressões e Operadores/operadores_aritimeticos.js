// multiplicalçao
console.log('Multiplicação: ', 2 * 4)

// divisão
console.log('Divisão: ', 12 / 2)

// soma
console.log('Soma: ', 12 + 3)

// subtração
console.log('Subtração: ', 12 - 2)

// resto da divisão
console.log('Resto da divisão: ', 12 % 3)

// incremento
let number = 1
console.log('Incremento: ', ++number)

// decremento
console.log('Decremento: ', --number)

// exponencial
console.log('Exponencial: ', 2 ** 4)